def get_translations(locale: str = "en") -> dict:
    """Get translations for a locale."""
    return TRANSLATIONS.get(locale, TRANSLATIONS.get("en", {}))

def get_category_translation(category_id: str, locale: str = "en") -> str:
    """Get translated category name."""
    translations = CATEGORY_TRANSLATIONS.get(locale, CATEGORY_TRANSLATIONS.get("en", {}))
    return translations.get(category_id, category_id.replace("_", " ").title())

def get_available_locales() -> list:
    """Get list of available locales."""
    return list(CATEGORY_TRANSLATIONS.keys())
